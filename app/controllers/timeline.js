/* controller js for timeline  */ 
app.controller('timeline' , function(Page, $scope , $http, article, $window){
	Page.setTitle("Timeline"); 
    $('body').css('pointer-events','none'); 										
	$('body').css('opacity','0.5'); 										
	$('.la-anim-10').show();
	setInterval(function(){
		$('body').css('pointer-events','auto'); 										
		$('body').css('opacity','1'); 										
		$('.la-anim-10').hide();
    }, 3000);

});